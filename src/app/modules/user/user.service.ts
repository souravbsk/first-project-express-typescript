import { sendImageToCloudinary } from '../../utils/sendImageToCloudinary';

const createStudentIntoDB = async (
  file: any,
  password: string,
  payload: TStudent,
) => {
  //create a new user
  const userData: Partial<TUser> = {};

  userData.password = password || (config.defaultPassword as string);

  // set student role
  userData.role = 'student';
  // set student email
  userData.email = payload.email;

  // year semester and 4  digit number
  const admissionSemester = await AcademicSemester.findById(
    payload.admissionSemester,
  );

  if (!admissionSemester) {
    throw new AppError(StatusCodes.NOT_FOUND, 'Admission semester not found');
  }

  // find department
  const academicDepartment = await AcademicDepartment.findById(
    payload.academicDepartment,
  );

  if (!academicDepartment) {
    throw new AppError(StatusCodes.NOT_FOUND, 'Academic department not found');
  }

  payload.academicFaculty = academicDepartment?.academicFaculty;
  //create a session
  const session = await mongoose.startSession();

  try {
    //start a transaction session
    session.startTransaction();

    if (admissionSemester) {
      // set manually generated id
      userData.id = await generateStudentId(admissionSemester);
    }

    if (file) {
      const imageName = `${userData.id}${payload.name.firstName}`;
      const path = file?.path;
      const { secure_url } = await sendImageToCloudinary(imageName, path);
      payload.profileImg = secure_url as string;
    }
    // send image to cloudinary
    // create  a user (transaction-1)
    const newUser = await User.create([userData], { session }); // output array

    // create a student

    if (!newUser?.length) {
      throw new AppError(StatusCodes.BAD_REQUEST, 'Failed to create user');
    }

    // set id , _id as user

    payload.id = newUser[0].id;
    payload.user = newUser[0]._id; // reference _id
    // create  a student (transaction-2)
    const newStudent = await Student.create([payload], { session }); // output array
    if (!newStudent.length) {
      throw new AppError(StatusCodes.BAD_REQUEST, 'Failed to create student');
    }

    // commit the transaction
    await session.commitTransaction();
    // close the session
    await session.endSession();

    return newStudent;
  } catch (error: any) {
    // abort the transaction
    await session.abortTransaction();
    // close the session
    await session.endSession();
    throw error;
  }
};

